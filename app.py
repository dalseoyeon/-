# 버튼
if st.button("운세 보기"):

    if birthday.strip() == "":
        st.warning("생일을 입력해주세요.")
    else:
        # 생일 기준으로 결과 고정
        random.seed(birthday)

        fortune = random.choice(fortunes)

        st.subheader("🌟 오늘의 운세")
        st.success(fortune)
